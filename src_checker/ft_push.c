#include "../push_swap.h"

void	ft_push(t_stack **s1, t_stack **s2)
{
	t_stack *temp;

	if (*s2)
	{
		temp = *s1;
		*s1 = *s2;
		*s2 = (*s2)->next;
		(*s1)->next = temp;
	}
}
